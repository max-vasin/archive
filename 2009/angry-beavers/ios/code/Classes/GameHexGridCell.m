#import "GameHexGridCell.h"

// ******** IMPLEMENTATION ********

@implementation GameHexGridCell

//#######################################################################################################################
// PROPERTIES
//#######################################################################################################################

//-----------------------------------------------------------------------------------------------------------------------
// SYNTHESIZED
//-----------------------------------------------------------------------------------------------------------------------

@synthesize propType	= m_type;
@synthesize propGrid	= m_grid;
@synthesize propUnit	= m_unit;
@synthesize propIndex	= m_index;

//#######################################################################################################################
// CONSTRUCTION
//#######################################################################################################################

//-----------------------------------------------------------------------------------------------------------------------
// Designated initializer
//-----------------------------------------------------------------------------------------------------------------------

- (id)initWithGrid:(GameHexGrid*)grid X:(oriUint)x Y:(oriUint)y Type:(enGameCellType)type
{
	if ( (self = [self init]) )
	{
		m_grid		= grid;
		m_index.x	= x;
		m_index.y	= y;
		m_type		= type;
	}
	return self;
}

//-----------------------------------------------------------------------------------------------------------------------
// Destructor
//-----------------------------------------------------------------------------------------------------------------------

- (void)dealloc
{
	[super dealloc];
}

@end
