// ******** IMPORTS ********

#import "OriSpriteAnimationPoint.h"

// ******** IMPLEMENTATION ********

@implementation OriSpriteAnimationPoint

//#######################################################################################################################
// PROPERTIES
//#######################################################################################################################

//-----------------------------------------------------------------------------------------------------------------------
// SYNTHESIZED
//-----------------------------------------------------------------------------------------------------------------------

@synthesize propName		= m_name;
@synthesize propPosition	= m_position;

//#######################################################################################################################
// CONSTRUCTION
//#######################################################################################################################

//-----------------------------------------------------------------------------------------------------------------------
// Returns new autoreleased point with XML data
//-----------------------------------------------------------------------------------------------------------------------

+ (id)pointWithXML:(OriXMLNode*)node
{
	return [[[self alloc] initWithXML:node] autorelease];
}

//#######################################################################################################################
// INITIALIZTION/DESTRUCTION
//#######################################################################################################################

//-----------------------------------------------------------------------------------------------------------------------
// Initializes new point with XML data
//-----------------------------------------------------------------------------------------------------------------------

- (id)initWithXML:(OriXMLNode*)node
{
	if ( (self = [self init]) )
	{
		m_name		 = [[node getFirstChildValue:@"Name" placeholder:@""] copy];
		
		m_position.x = [node getFirstChildValueInt:@"X" placeholder:0];
		m_position.y = [node getFirstChildValueInt:@"Y" placeholder:0];
	}
	
	return self;
}

//-----------------------------------------------------------------------------------------------------------------------
// Destructor
//-----------------------------------------------------------------------------------------------------------------------

- (void)dealloc
{
	[m_name release];
	
	[super dealloc];
}

@end
